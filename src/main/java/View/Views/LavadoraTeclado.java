        String valorPrecio = PrecioLabel.getText();
        String valorPeso = PesoLabel.getText();
        String valorCarga = CargaLabel.getText();
        String valorColor = ComboBoxColor.getSelectedItem().toString();
        String valorConsumo = ComboBoxConsumo.getSelectedItem().toString();

        boolean resultado = listaDeLavadoras.crearLavadora(valorPrecio, valorColor, valorConsumo, valorPeso, valorCarga);
        if (resultado) {
            JOptionPane.showMessageDialog(null, "Lavadora registrada con Ã©xito.", "Registro Exitoso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Error al registrar la lavadora. Verifique los datos ingresados.", "Error", JOptionPane.ERROR_MESSAGE);
        }

